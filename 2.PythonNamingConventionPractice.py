# 2. Python Naming Convention Practice
# Variable Names:
# Use lowercase letters with words separated by underscores .

# Constant Names:
# Use uppercase letters with words separated by underscores (e.g., PI_VALUE, MAX_SPEED).

# Function and Class Names:
# Use lowercase with words separated by underscores .
# Consider using PascalCase (first letter of each word capitalized) for public class names (e.g., MyClass).

# Package and Module Names:
# Use lowercase letters with words separated by underscore

